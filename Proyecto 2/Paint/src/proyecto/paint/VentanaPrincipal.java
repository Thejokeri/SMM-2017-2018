    /**
     * Funcion seno
     * @return the salida
     */
    private LookupTable seno(double w){
        double K = 255.0;
         
        byte[] lt = new byte[256];
        
        w = w / K;
        
        for(int i = 0; i <= 255; i++){
            lt[i] = (byte)(K * Math.abs(Math.sin(Math.toRadians(i * w))));
        }
        
        ByteLookupTable salida = new ByteLookupTable(0, lt);
        return salida;
    }
    
