    /**
     * Funcion parabola
     * @return the salida
     */
    private LookupTable parabola(){
        double K = 255.0;
         
        byte[] lt = new byte[256];
        
        double a = 4.0/255.0;
        
        for(int i = 0; i <= 255; i++){
            lt[i] = (byte)(a*Math.pow(i, 2) - 4*i + 255);
        }
        
        ByteLookupTable salida = new ByteLookupTable(0, lt);
        return salida;
    }
    
