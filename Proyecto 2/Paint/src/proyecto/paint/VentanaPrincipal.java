    private void DialogTamanio(){
        JFrame frame = new JFrame();
        frame.getContentPane().setLayout(new BorderLayout());
        
        frame.setTitle("Tamaño del lienzo");
        frame.setSize(500,100);
        
        JPanel panelsuperior = new JPanel();
        panelsuperior.setLayout(new FlowLayout());
        
        JPanel panelinferior = new JPanel();
        panelinferior.setLayout(new FlowLayout());
        
        JLabel ancho = new JLabel("Ancho");
        JTextField anchotexto = new JTextField(10);
        JLabel alto = new JLabel("Alto");
        JTextField altotexto = new JTextField(10);
        
        panelsuperior.add(ancho);
        panelsuperior.add(anchotexto);
        panelsuperior.add(alto);
        panelsuperior.add(altotexto);
        
        javax.swing.JButton aceptar = new javax.swing.JButton();
        aceptar.setText("Aceptar");
        javax.swing.JButton cancelar = new javax.swing.JButton();
        cancelar.setText("Cancelar");
        
        panelinferior.add(cancelar);
        panelinferior.add(aceptar);
       
        frame.add(panelsuperior,BorderLayout.PAGE_START);
        frame.add(panelinferior, BorderLayout.PAGE_END);
        frame.setVisible(true);
        
        aceptar.addActionListener(new java.awt.event.ActionListener() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                AceptarActionPerformed(evt);
            }

            private void AceptarActionPerformed(ActionEvent evt) {
                int ancho = Integer.parseInt(anchotexto.getText());
                int alto = Integer.parseInt(altotexto.getText());
                
                if(ancho > 0 && alto > 0){
                    setAncho(ancho);
                    setAlto(alto);
                    MenuNuevoActionPerformed(null);
                    frame.dispose();
                }else{
                    DialogTamanio();
                }
            }
        });
        
        cancelar.addActionListener(new java.awt.event.ActionListener() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                CancelarActionPerformed(evt);
            }

            private void CancelarActionPerformed(ActionEvent evt) {
                setAncho(0);
                setAlto(0);
                frame.dispose();
            }
        });
    }
    
